r=0.02;
t=-5:r:5;
N=200;
W=2*pi*1;
k=-N:N;
w=k*W/N;
f1=1/2*exp(-2*t).*heaviside(t);
F=r*f1*exp(-j*t'*w);
F1=abs(F);
P1=angle(F);
subplot(311);
plot(t,f1);
grid;
xlabel('t');
ylabel('f(t)');
title('f(t)');
subplot(312);
plot(w,F1);
xlabel('w');
grid;
ylabel('F(jw)');
subplot(313);
plot(w,P1*180/pi);
grid;
xlabel('w');
ylabel('P(¶È)');
