 r=0.01;
t=-15:r:15;
f=sin(t)./t;
f1=pi*(heaviside(t+1)-heaviside(t-1));
N=500;
W=5*pi*1;
k=-N:N;
w=k*W/N;
F=r*sinc(t/pi)*exp(-j*t'*w);
F1=r*f1*exp(-j*t'*w);
subplot(221);
plot(t,f);
xlabel('t');
ylabel('f(t)');
subplot(222);
plot(w,F);
axis([-2 2 -1 4]);
xlabel('w');
ylabel('F(w)');
subplot(223);
plot(t,f1);
axis([-2 2 -1 4]);
xlabel('t');
ylabel('f1(t)');
subplot(224);
plot(w,F1);
axis([-20 20 -3 7]);
xlabel('w');
ylabel('F1(w)');
